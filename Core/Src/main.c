int8_t print_long_text(i2cLcd_HandleTypeDef *h_lcd, char * text){
    uint8_t i = 0u;
    int8_t retVal = 1;

    if(text == NULL) {
        retVal = -1;
    }

    while(text[i]){
        i2cLcd_SendChar(h_lcd, text[i]);
        if(i == 15) {
                i2cLcd_SetCursorPosition(h_lcd, 0x40); // 2nd line, 1st character address
        }
        i++;
    }

    return retVal;
}

